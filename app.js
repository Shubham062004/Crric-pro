// Virendra Sechwag
let virendraphoto=document.getElementById("sehwag_photo")
let virendra=document.getElementById("virendra")
virendra.addEventListener("click",function(){
    virendraphoto.style.display="block";
    virendraphoto.style.transform="translate(5em,-20em)"
    virendraphoto.onmouseleave=()=>{
        virendraphoto.style.display="none";
    }
})

